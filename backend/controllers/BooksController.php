<?php

namespace backend\controllers;

use backend\models\BookCategory;
use backend\models\Books;
use backend\models\BooksSearch;
use backend\models\Categories;
use yii\web\Controller;
use yii\web\NotFoundHttpException;
use yii\filters\VerbFilter;

/**
 * BooksController implements the CRUD actions for Books model.
 */
class BooksController extends Controller
{
    /**
     * @inheritDoc
     */

    public function behaviors()
    {
        return array_merge(
            parent::behaviors(),
            [
                'verbs' => [
                    'class' => VerbFilter::class,
                    'actions' => [
                        'delete' => ['POST'],
                    ],
                ],
            ]
        );
    }

    /**
     * Lists all Books models.
     *
     * @return string
     */
    public function actionIndex()
    {
        $searchModel = new BooksSearch();
        $dataProvider = $searchModel->search($this->request->queryParams);

        return $this->render('index', [
            'searchModel' => $searchModel,
            'dataProvider' => $dataProvider,
        ]);
    }

    /**
     * Displays a single Books model.
     * @param int $id ID
     * @return string
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionView($id)
    {
        $book = Books::findOne($id);
        return $this->render('view', [
            'model' => $this->findModel($id),
            'book' => $book,
        ]);
    }

    public function actionParse()
    {

        $file = file_get_contents('../books.json');
        $books = json_decode($file, true);

        foreach ($books as $bookData) {

            $book = new Books();
            $BookID = $book->createBook($bookData);

            if ($BookID) {
                if (!isset($bookData['categories'])) $bookData['categories'] = array('NEW!!!');
                foreach ($bookData['categories'] as $categoryData) {
                    if ($categoryData === '') $categoryData = 'NEW!!!';
                    $CategoryID = $book->createCategory($categoryData);
                    $book->createBookToCategory($BookID, $CategoryID);
                }
                if (!isset($bookData['authors'])) $bookData['authors'] = array('Unnamed');
                foreach ($bookData['authors'] as $authorData) {
                    if ($authorData === '') $authorData = 'Unnamed';
                    $AuthorID = $book->createAuthor($authorData);
                    $book->createBookToAuthor($BookID, $AuthorID);
                }
            }
        }
        echo "DONE!!!";
    }

    /**
     * Creates a new Books model.
     * If creation is successful, the browser will be redirected to the 'view' page.
     * @return string|\yii\web\Response
     */
    public function actionCreate()
    {
        $model = new Books();

        if ($this->request->isPost) {
            if ($model->load($this->request->post()) && $model->save()) {
                return $this->redirect(['view', 'id' => $model->id]);
            }
        } else {
            $model->loadDefaultValues();
        }

        return $this->render('create', [
            'model' => $model,
        ]);
    }

    /**
     * Updates an existing Books model.
     * If update is successful, the browser will be redirected to the 'view' page.
     * @param int $id ID
     * @return string|\yii\web\Response
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionUpdate($id)
    {
        $model = $this->findModel($id);

        if ($this->request->isPost && $model->load($this->request->post()) && $model->save()) {
            return $this->redirect(['view', 'id' => $model->id]);
        }

        return $this->render('update', [
            'model' => $model,
        ]);
    }

    /**
     * Deletes an existing Books model.
     * If deletion is successful, the browser will be redirected to the 'index' page.
     * @param int $id ID
     * @return \yii\web\Response
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionDelete($id)
    {
        $this->findModel($id)->delete();

        return $this->redirect(['index']);
    }

    /**
     * Finds the Books model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param int $id ID
     * @return Books the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id)
    {
        if (($model = Books::findOne(['id' => $id])) !== null) {
            return $model;
        }

        throw new NotFoundHttpException('The requested page does not exist.');
    }
}
